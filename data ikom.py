# Tipe Data String
foo = "Belajar Python adalah perjalanan yang menyenangkan dan Sangan Penting"
print(foo)

# Tipe Data Integer
foo = 2024
print(foo)

# Tipe Data Float
foo = 155.55555
print(foo)

# Tipe Data Complex Number
foo = 2j
print(foo)

# Tipe Data Boolean
foo = False
print(foo)

# Tipe Data List
foo = ["saya", "ingin", "makan", "apel"]
print(foo)

# Tipe Data Tuple
foo = ("saya", "suka", "biru")
print(foo)

# Tipe Data Set
foo = {"motor", "mobil", "rumah"}
print(foo)

# Tipe Data Dictionary
foo = {"nama": "Serlia", "umur": 19, "status": "mahasiswa universitas sulawesi barat"}
print(foo)